  static List<String> get continents => countriesByContinent.keys.toList();
  
  static List<Map<String, dynamic>> getCountriesByContinent(String continent) {
    return countriesByContinent[continent] ?? [];
  }

  static List<Map<String, dynamic>> searchCountries(String query) {
    final results = <Map<String, dynamic>>[];
    countriesByContinent.forEach((continent, countries) {
      for (var country in countries) {
        if (country['name'].toString().toLowerCase().contains(query.toLowerCase()) ||
            country['capital'].toString().toLowerCase().contains(query.toLowerCase()) ||
            country['languages'].any((lang) => lang.toString().toLowerCase().contains(query.toLowerCase()))) {
          results.add({
            ...country,
            'continent': continent,
          });
        }
      }
    });
    return results;
  }

  static Map<String, int> getLanguageStats() {
    final languageCount = <String, int>{};
    countriesByContinent.forEach((continent, countries) {
      for (var country in countries) {
        for (var language in country['languages']) {
          languageCount[language] = (languageCount[language] ?? 0) + 1;
        }
      }
    });
    return languageCount;
  }
